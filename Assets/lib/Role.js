class Role {
    constructor(ID, title, salary, department_ID) {
        this.ID = ID;
        this.title = title;
        this.salary = salary;
        this.department_ID = department_ID;
    }
    getName() {
        return this.ID;
    }
    getName() {
        return this.title;
    }
    getID() {
        return salary;
    }
    getEmail() {
        return department_ID;
    }
}
module.exports = Role;